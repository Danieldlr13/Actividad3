    private void btnCalcular2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcular2ActionPerformed
        double base,altura,area,perimetro;
        
        base = Double.parseDouble(txtBase2.getText());
        altura = Double.parseDouble(txtAltura2.getText());
         
        area = base * altura;       
        perimetro = 2*(base+altura);
        
        txtArea2.setText(String.valueOf(area));
        txtPerimetro2.setText(String.valueOf(perimetro));
        
        
    }//GEN-LAST:event_btnCalcular2ActionPerformed

    private void btnCalcular3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcular3ActionPerformed
        double lado,area,perimetro;
        
        lado = Double.parseDouble(txtLado3.getText());
        
        area = lado*lado;
        perimetro = 4*lado;
              
        txtArea3.setText(String.valueOf(area));
        txtPerimetro3.setText(String.valueOf(perimetro));

    }//GEN-LAST:event_btnCalcular3ActionPerformed

