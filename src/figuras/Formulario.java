    private void btnCalcular4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcular4ActionPerformed
        
        double base, altura, area, perimetro;
                
        base = Double.parseDouble(txtBase4.getText());       
        altura = Double.parseDouble(txtAltura4.getText());        
        area = base * altura /2;
        perimetro = base + altura + (Math.pow(base*base + altura*altura, 0.5));
        
        txtPerimetro4.setText(String.valueOf(perimetro));
        txtArea4.setText(String.valueOf(area));
        
        if ((base == altura) && (base == (Math.pow(base*base + altura*altura, 0.5)) && (altura 
                == (Math.pow(base*base + altura*altura, 0.5)))))
            {txtTipo.setText("Es un triángulo equilátero");}       
        
        else if 
            ((base != altura) && (base != ((Math.pow(base*base + altura*altura, 0.5))) && 
            (altura != (Math.pow(base*base + altura*altura, 0.5))))){ 
            txtTipo.setText("Es un triángulo escaleno");}
        else 
            txtTipo.setText("Es un triángulo isósceles");      
        
    }//GEN-LAST:event_btnCalcular4ActionPerformed

